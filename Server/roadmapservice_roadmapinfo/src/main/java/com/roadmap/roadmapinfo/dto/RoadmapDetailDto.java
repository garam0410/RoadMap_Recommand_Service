package com.roadmap.roadmapinfo.dto;

public class RoadmapDetailDto {
	private String rid;
	private String root;
	private String r_info;
	private String trunk;
	private String t_info;
	private String branch;
	private String b_info;
	private String leaf;
	
	public void setRid(String rid) {
		this.rid = rid;
	}
	public String getRid() {
		return rid;
	}
	public String getRoot() {
		return root;
	}
	public void setRoot(String root) {
		this.root = root;
	}
	public String getR_info() {
		return r_info;
	}
	public void setR_info(String r_info) {
		this.r_info = r_info;
	}
	public String getTrunk() {
		return trunk;
	}
	public void setTrunk(String trunk) {
		this.trunk = trunk;
	}
	public String getT_info() {
		return t_info;
	}
	public void setT_info(String t_info) {
		this.t_info = t_info;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public String getB_info() {
		return b_info;
	}
	public void setB_info(String b_info) {
		this.b_info = b_info;
	}
	public String getLeaf() {
		return leaf;
	}
	public void setLeaf(String leaf) {
		this.leaf = leaf;
	}
}
